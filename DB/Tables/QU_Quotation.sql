    FOREIGN KEY (ServiceId) REFERENCES [dbo].[REF_SERVICE](Id),
	FOREIGN KEY (BillingMethodId) REFERENCES [dbo].[QU_BillMethod](Id),
	FOREIGN KEY (BillingPaidById) REFERENCES [dbo].[QU_PaidBy](Id),
	FOREIGN KEY (CustomerId) REFERENCES [dbo].[CU_Customer](Id),
	FOREIGN KEY (SupplierId) REFERENCES [dbo].[SU_Supplier](Id),
	FOREIGN KEY (FactoryId) REFERENCES [dbo].[SU_Supplier](Id),
	FOREIGN KEY (OfficeId) REFERENCES [dbo].[REF_LOCATION](Id),
	FOREIGN KEY (CurrencyId) REFERENCES [dbo].[REF_CURRENCY](Id),
	FOREIGN KEY (IdStatus) REFERENCES QU_Status(Id),
	FOREIGN KEY (BillingEntity) REFERENCES [REF_Billing_Entity](Id),
	FOREIGN KEY (PaymentTerms) REFERENCES [REF_InvoiceType](Id)
 )